-- 1. Cognitive test sessions
create type cognitive_test_category as enum (
  'logique', 'math', 'verbal', 'spatial', 'mixte'
);

create type cognitive_test_status as enum (
  'in_progress', 'completed', 'abandoned', 'timeout'
);

create table public.cognitive_test_sessions (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references public.profiles(id) on delete cascade,
  category cognitive_test_category not null,
  difficulty public.difficulty_level not null default 'intermediaire',
  status cognitive_test_status not null default 'in_progress',
  score int,
  max_score int,
  question_count int not null default 10,
  time_limit_minutes int not null default 15,
  time_spent_seconds int,
  current_question int not null default 0,
  started_at timestamptz not null default now(),
  ended_at timestamptz,
  created_at timestamptz not null default now()
);

create index idx_cognitive_test_sessions_user on public.cognitive_test_sessions(user_id);

-- 3. Cognitive test questions (generated by AI, stored for history)
create table public.cognitive_test_questions (
  id uuid primary key default gen_random_uuid(),
  session_id uuid not null references public.cognitive_test_sessions(id) on delete cascade,
  question_text text not null,
  options jsonb not null,
  correct_index int not null,
  user_answer_index int,
  is_correct boolean,
  explanation text,
  difficulty public.difficulty_level not null default 'intermediaire',
  points int not null default 1,
  order_num int not null,
  created_at timestamptz not null default now()
);

create index idx_cognitive_test_questions_session on public.cognitive_test_questions(session_id);

-- 4. Skills assessment categories
create table public.skills_assessment_categories (
  id uuid primary key default gen_random_uuid(),
  name text not null,
  description text,
  icon text,
  display_order int not null default 0,
  is_active boolean not null default true,
  created_at timestamptz not null default now()
);

-- 5. Skills assessment questions (predefined)
create table public.skills_assessment_questions (
  id uuid primary key default gen_random_uuid(),
  category_id uuid not null references public.skills_assessment_categories(id) on delete cascade,
  question_text text not null,
  order_num int not null default 0,
  weight int not null default 1,
  is_active boolean not null default true,
  created_at timestamptz not null default now()
);

create index idx_saq_category on public.skills_assessment_questions(category_id);

-- 6. Skills assessment sessions
create type skills_assessment_status as enum (
  'draft', 'in_progress', 'completed'
);

create table public.skills_assessments (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references public.profiles(id) on delete cascade,
  status skills_assessment_status not null default 'draft',
  overall_score int,
  started_at timestamptz,
  completed_at timestamptz,
  created_at timestamptz not null default now()
);

create index idx_skills_assessments_user on public.skills_assessments(user_id);

-- 7. Skills assessment answers
create table public.skills_assessment_answers (
  id uuid primary key default gen_random_uuid(),
  assessment_id uuid not null references public.skills_assessments(id) on delete cascade,
  question_id uuid not null references public.skills_assessment_questions(id) on delete cascade,
  rating int not null check (rating >= 1 and rating <= 5),
  created_at timestamptz not null default now(),
  unique (assessment_id, question_id)
);

-- 8. Skills assessment results (AI-generated report)
create table public.skills_assessment_results (
  id uuid primary key default gen_random_uuid(),
  assessment_id uuid not null references public.skills_assessments(id) on delete cascade unique,
  category_scores jsonb not null,
  global_summary text,
  strengths jsonb,
  areas_for_improvement jsonb,
  training_gap_analysis jsonb,
  generated_at timestamptz not null default now()
);

