-- Cognitive tests: users see their own sessions
create policy "Users read own test sessions"
  on public.cognitive_test_sessions for select
  using (auth.uid() = user_id);

create policy "Users insert own test sessions"
  on public.cognitive_test_sessions for insert
  with check (auth.uid() = user_id);

create policy "Users update own test sessions"
  on public.cognitive_test_sessions for update
  using (auth.uid() = user_id);

-- Test questions: read if own session
create policy "Users read own test questions"
  on public.cognitive_test_questions for select
  using (exists (
    select 1 from public.cognitive_test_sessions s
    where s.id = session_id and s.user_id = auth.uid()
  ));

create policy "System insert test questions"
  on public.cognitive_test_questions for insert
  with check (exists (
    select 1 from public.cognitive_test_sessions s
    where s.id = session_id and s.user_id = auth.uid()
  ));

-- Skills categories/questions: public read
create policy "Anyone read categories"
  on public.skills_assessment_categories for select
  using (true);

create policy "Anyone read questions"
  on public.skills_assessment_questions for select
  using (true);

-- Skills assessments: user owns their own
create policy "Users read own assessments"
  on public.skills_assessments for select
  using (auth.uid() = user_id);

create policy "Users insert own assessments"
  on public.skills_assessments for insert
  with check (auth.uid() = user_id);

create policy "Users update own assessments"
  on public.skills_assessments for update
  using (auth.uid() = user_id);

-- Answers: own assessment
create policy "Users manage own answers"
  on public.skills_assessment_answers for all
  using (exists (
    select 1 from public.skills_assessments a
    where a.id = assessment_id and a.user_id = auth.uid()
  ));

-- Results: own assessment
create policy "Users read own results"
  on public.skills_assessment_results for select
